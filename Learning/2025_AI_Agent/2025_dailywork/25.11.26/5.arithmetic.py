# 사직연산 활용해보기

# 1. 간단하게 구성해보기
x = 8
y = 3
result = x ** y
print(f"{x}의 {y}제곱은 {result}입니다.")
# 8의 3제곱은 512입니다.

# 2. 심화해보기
a = 10
b = 4
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} × {b} = {a * b}")
print(f"{a} ÷ {b} = {a / b:.2f}")
print(f"{a}의 {b}제곱 = {a ** b}")   # 연산자

'''
=== 결과 ===
10 + 4 = 14
10 - 4 = 6
10 × 4 = 40
10 ÷ 4 = 2.50
10의 4제곱 = 10000
'''