# python 변수와 자료형 정리하기

'''
정수형(int)  | a = 10          | 소수점 없는 정수형 숫자 데이터 (0,1,2,3,)
실수형(float)| b = 3.14        | 소수점을 포함한 숫자형 데이터
문자열(str)  | c = "AI"        | 문자,문자열을 표현 ("",'')
불(bool)    | is_ai = True    | 참(True), 거짓(False) 논리형 데이터
'''

a = 10
b = 3.14
c = "AI"
is_ai = True

print(a, b, c, is_ai)
print(type(a), type(b), type(c), type(is_ai))

'''
=== 결과 ===
10 3.14 AI True
<class 'int'> <class 'float'> <class 'str'> <class 'bool'>

'''